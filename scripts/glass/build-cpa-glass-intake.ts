type GlassRow = {
  vendor_pn: string;
  brand: string;
  glass_type: string;
  material: string;
  color: string;
  thickness_mm: number | null;
  max_width_mm: number | null;
  min_width_mm: number | null;
  height_mm: number | null;
  hole_count: number | null;
  compatible_models: string[];
  is_pack?: boolean;
  pack_qty?: number;
  phase: string;
  notes?: string;
  is_accessory?: boolean;
};

function slugify(s: string): string {
  return s
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

function oemDisplay(vendorPn: string, brand: string): string {
  const pn = vendorPn.trim();
  if (brand === 'Cab Glass Accessory') {
    // House accessory catalog codes use SY prefix — strip for customer display.
    if (pn.toUpperCase().startsWith('SY')) return pn.slice(2);
    return pn;
  }
  if (pn.startsWith('KB')) return pn.slice(2);
  if (pn.startsWith('BC')) return pn.slice(2);
  if (pn.startsWith('CT')) return pn.slice(2);
  if (pn.startsWith('CS')) return pn.slice(2);
  if (pn.startsWith('JD')) return pn.slice(2);
  if (pn.startsWith('TK')) return pn.slice(2);
  return pn;
}

function brandSlug(brand: string): string {
  if (brand === 'Cab Glass Accessory') return 'glass';
  if (brand === 'John Deere') return 'john-deere';
  return slugify(brand);
}

function typeSlug(glassType: string): string {
  return slugify(glassType.replace(/_/g, '-'));
}

function buildSlug(row: GlassRow): string {
  const oem = oemDisplay(row.vendor_pn, row.brand).toLowerCase();
  const oemSlug = slugify(oem);
  if (row.is_accessory) {
    return `${typeSlug(row.glass_type)}-${oemSlug}`;
  }
  return `${brandSlug(row.brand)}-${oemSlug}-${typeSlug(row.glass_type)}`;
}

