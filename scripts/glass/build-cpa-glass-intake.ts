function humanType(glassType: string): string {
  return glassType
    .split('_')
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(' ');
}

function main() {
  const outDir = path.resolve(process.cwd(), 'data/glass');
  fs.mkdirSync(outDir, { recursive: true });

  const seen = new Set<string>();
  const intake = [];

  for (const row of ROWS) {
    if (seen.has(row.vendor_pn)) {
      console.warn(`Duplicate vendor_pn skipped: ${row.vendor_pn}`);
      continue;
    }
    seen.add(row.vendor_pn);

    const oem_pn_display = oemDisplay(row.vendor_pn, row.brand);
    const slug = buildSlug(row);
    const publish_status = row.is_pack ? 'skip_retail' : 'pending';
    const is_glass_accessory = Boolean(row.is_accessory);
    const confirm_freight = !is_glass_accessory && !row.is_pack;

    intake.push({
      vendor_pn: row.vendor_pn,
      brand: row.brand,
      oem_pn_display,
      slug,
      glass_type: row.glass_type,
      glass_type_label: humanType(row.glass_type),
      material: row.material,
      color: row.color,
      thickness_mm: row.thickness_mm,
      max_width_mm: row.max_width_mm,
      min_width_mm: row.min_width_mm,
      height_mm: row.height_mm,
      hole_count: row.hole_count,
      compatible_models: row.compatible_models,
      is_pack: Boolean(row.is_pack),
      pack_qty: row.pack_qty ?? null,
      phase: row.phase,
      publish_status,
      is_glass_accessory,
      confirm_freight,
      notes: row.notes ?? '',
    });
  }

  const phases: Record<string, string[]> = {};
  for (const item of intake) {
    if (item.publish_status === 'skip_retail') continue;
    if (!phases[item.phase]) phases[item.phase] = [];
    phases[item.phase].push(item.vendor_pn);
  }

  const csvHeader = [
    'vendor_pn',
    'brand',
    'oem_pn_display',
    'slug',
    'glass_type',
    'material',
    'color',
    'thickness_mm',
    'max_width_mm',
    'min_width_mm',
    'height_mm',
    'hole_count',
    'compatible_models',
    'is_pack',
    'pack_qty',
    'phase',
    'publish_status',
    'is_glass_accessory',
    'confirm_freight',
    'notes',
  ];

  const csvLines = [
    csvHeader.join(','),
    ...intake.map((r) =>
      [
        r.vendor_pn,
        r.brand,
        r.oem_pn_display,
        r.slug,
        r.glass_type,
        r.material,
        r.color,
        r.thickness_mm ?? '',
        r.max_width_mm ?? '',
        r.min_width_mm ?? '',
        r.height_mm ?? '',
        r.hole_count ?? '',
        `"${r.compatible_models.join('|')}"`,
        r.is_pack,
        r.pack_qty ?? '',
        r.phase,
        r.publish_status,
        r.is_glass_accessory,
        r.confirm_freight,
        `"${(r.notes || '').replace(/"/g, '""')}"`,
      ].join(',')
    ),
  ];

  fs.writeFileSync(path.join(outDir, 'cpa-glass-intake.json'), JSON.stringify(intake, null, 2));
  fs.writeFileSync(path.join(outDir, 'cpa-glass-intake.csv'), csvLines.join('\n') + '\n');
  fs.writeFileSync(path.join(outDir, 'cpa-glass-phases.json'), JSON.stringify(phases, null, 2));

  const readme = `# CPA Cab Glass Intake

Source: vendor QRG \`2110094T\` (Windshields and Glass Product Catalog).
Built by: \`npx tsx scripts/glass/build-cpa-glass-intake.ts\`

## Files

- \`cpa-glass-intake.csv\` / \`.json\` — one row per vendor PN
- \`cpa-glass-phases.json\` — vendor PNs per publish phase (excludes \`skip_retail\` packs)

## Prefix / slug rules

| Prefix | Brand | OEM display |
|--------|-------|-------------|
| BC | Bobcat | strip BC |
| CT | Caterpillar | strip CT |
| CS | Case | strip CS |
| JD | John Deere | strip JD |
| KB* | Kubota | strip leading KB |
| TK | Takeuchi | strip TK |
| SY* | Cab Glass Accessory | strip SY (vendor_pn keeps SY) |

## Publish status

- \`pending\` — eligible for site publish
- \`skip_retail\` — 25-packs (\`*PK\`); B2B only, not published to PDPs

## Phases

1. Accessories (seals, cord, adhesive, cleaner, hardware)
2. Bobcat
3. Caterpillar
4. Case
5. John Deere
6. Kubota
7. Takeuchi
`;

  fs.writeFileSync(path.join(outDir, 'README.md'), readme);

  const flagships = [
    'BC7120401',
    'CT345-6230',
    'CS84344565',
    'JDT312628',
    'KBRG158-46930',
    'TK03586-00076',
  ];

  console.log(`Wrote ${intake.length} rows to data/glass/`);
  console.log('Phase counts:', Object.fromEntries(
    Object.entries(phases).map(([k, v]) => [k, v.length])
  ));
  console.log('\nFlagship QA:');
  for (const pn of flagships) {
    const row = intake.find((r) => r.vendor_pn === pn);
    if (!row) {
      console.error(`  FAIL missing ${pn}`);
      continue;
    }
    console.log(
      `  OK ${pn} → ${row.slug} | oem=${row.oem_pn_display} | models=${row.compatible_models.length}`
    );
  }
}

main();